     */
    public function store(Request $request)
    {
        $blog = Blog::create([
          'user_id' => auth::id(),
          'title' => $request->title,
          'slug' => SlugService::createSlug(Blog::class, 'slug',$request->title),
          'desc' => $request->desc,
        ]);
        return redirect()->route('blog.index')->with('success', 'Berhasil');

    }

